package projetos;

public class DescricaoProjetos {

		private String ProjetosSociais;
		private String ProjetosCulturais;
		private String ProjetosPesquisas;
		private String ProjetosEmpresariais;
		private String ProjetosInvestimentos;
		
		public String getProjetosSociais() {
			return ProjetosSociais;
		}
		public void setProjetosSociais(String projetosSociais) {
			ProjetosSociais = projetosSociais;
		}
		public String getProjetosCulturais() {
			return ProjetosCulturais;
		}
		public void setProjetosCulturais(String projetosCulturais) {
			ProjetosCulturais = projetosCulturais;
		}
		public String getProjetosPesquisas() {
			return ProjetosPesquisas;
		}
		public void setProjetosPesquisas(String projetosPesquisas) {
			ProjetosPesquisas = projetosPesquisas;
		}
		public String getProjetosEmpresariais() {
			return ProjetosEmpresariais;
		}
		public void setProjetosEmpresariais(String projetosEmpresariais) {
			ProjetosEmpresariais = projetosEmpresariais;
		}
		public String getProjetosInvestimentos() {
			return ProjetosInvestimentos;
		}
		public void setProjetosInvestimentos(String projetosInvestimentos) {
			ProjetosInvestimentos = projetosInvestimentos;
		}
		
	}













